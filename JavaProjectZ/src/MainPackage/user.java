    public void User_Load(){
        int c;
        try {
            pst = con.prepareStatement("SELECT * FROM users");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector ();
                
                for(int i=1;i<=c;i++){
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("adress"));
                    v2.add(rs.getString("uname"));
                    v2.add(rs.getString("utype"));
                    v2.add(rs.getString("systemcap"));
                    v2.add(rs.getString("invertortype"));
                    v2.add(rs.getString("panneltype"));
                    v2.add(rs.getString("salesman"));
                }
                
                d.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            System.getLogger(user.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }

