    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
     
       
      String name = jTextField1.getText();
      String sql = "SELECT id,name,phone,adress,uname,utype,systemcap,invertortype,panneltype,salesman FROM users WHERE name = '"+ name +"'" ;
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Id");
            model.addColumn("Name");
            model.addColumn("Phone");
            model.addColumn("Adress");
            model.addColumn("Username");
            model.addColumn("Usertype");
            model.addColumn("SystemCap");
            model.addColumn("Invertor");
            model.addColumn("PannelType");
            model.addColumn("Salesman");

while (rs.next()) {
        model.addRow(new Object[]{
        rs.getInt("id"),
        rs.getString("name"),
        rs.getString("phone"),
        rs.getString("adress"),
        rs.getString("uname"),
        rs.getString("utype"),
        rs.getString("systemcap"),
        rs.getString("invertortype"),
        rs.getString("panneltype"),
        rs.getString("salesman")
    });
}

jTable1.setModel(model);


        } catch (SQLException ex) {
            System.getLogger(user.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
      
    }//GEN-LAST:event_jButton6ActionPerformed

